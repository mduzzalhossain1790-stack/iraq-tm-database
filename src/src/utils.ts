export function getStoredCertificates(): TrademarkCertificate[] {
  try {
    const saved = localStorage.getItem("iraq_trademarks_db");
    if (!saved) {
      localStorage.setItem("iraq_trademarks_db", JSON.stringify(INITIAL_TRADEMARKS));
      return INITIAL_TRADEMARKS;
    }
    return JSON.parse(saved);
  } catch (e) {
    return INITIAL_TRADEMARKS;
  }
}

export function registerNewTrademark(item: TrademarkCertificate): TrademarkCertificate {
  const current = getStoredCertificates();
  
  // Clean potential duplication
  const filtered = current.filter(
    c => c.certificateNumber.toLowerCase().trim() !== item.certificateNumber.toLowerCase().trim()
  );

  const newItem: TrademarkCertificate = {
    ...item,
    gazetteNumber: item.gazetteNumber || `Official Gazette Issue 2026/${Math.floor(10 + Math.random() * 80)}`,
    isVerified: true
  };

  const updated = [...filtered, newItem];
  try {
    localStorage.setItem("iraq_trademarks_db", JSON.stringify(updated));
  } catch (e) {
    console.error("Storage error:", e);
  }
  return newItem;
}

export function queryTrademarkDatabase(query: string): TrademarkCertificate[] {
  const all = getStoredCertificates();
  if (!query || query.trim() === "") return all;

  const cleanQuery = query.toLowerCase().trim();
  return all.filter((c) => {
    return (
      c.trademarkNameEn.toLowerCase().includes(cleanQuery) ||
      c.trademarkNameAr.includes(cleanQuery) ||
      c.certificateNumber.toLowerCase().includes(cleanQuery) ||
      c.applicationNumber.toLowerCase().includes(cleanQuery) ||
      c.proprietorNameEn.toLowerCase().includes(cleanQuery) ||
      c.proprietorNameAr.includes(cleanQuery)
    );
  });
}

export function helperAutoTranslateBrand(brand: string): { en: string; ar: string } {
  const clean = brand.trim();
  const lower = clean.toLowerCase();
  
  // Custom dictionary for common Iraqi brand words
  const dict: Record<string, string> = {
    "sindibad": "السندباد",
    "sweets": "حلويات",
    "bakery": "مخبز",
    "al-saeed": "السعيد",
    "mesopotamia": "بلاد الرافدين",
    "babylon": "بابل",
    "baghdad": "بغداد",
    "basra": "البصرة",
    "trading": "للتجارة",
    "food": "المواد الغذائية",
    "beverage": "المشروبات",
    "group": "مجموعة",
    "company": "شركة",
    "industries": "صناعات",
    "national": "الوطنية",
    "oil": "النفط",
    "dates": "التمور",
    "iraq": "العراق",
    "karada": "الكرادة",
    "mansour": "المنصور",
    "water": "مياه"
  };

  let arResult = "";
  if (lower.includes("sindibad")) arResult = "حلويات ومخابز السندباد";
  else if (lower.includes("babylon")) arResult = "شركة بابل الوطنية للمشروبات والأغذية";
  else if (lower.includes("mesopotamia")) arResult = "شركة الرافدين للتجارة العامة والاستثمار";
  else if (lower.includes("basra")) arResult = "شركة تمور ومواد البصرة الغذائية المحدودة";
  else if (lower.includes("baghdad")) arResult = "مجموعة بغداد الوطنية المشتركة للتسويق";
  else if (lower.includes("hilal")) arResult = "مجموعة الهلال للتجارة والمقاولات العامة";
  else if (clean.match(/[\u0600-\u06FF]/)) {
    // Input is already Arabic, transliterate standard mock English
    arResult = clean;
    const words = clean.split(/\s+/);
    const mockEnVec = words.map(w => {
      // Very simple dummy romanization helper
      return "IRAQ-" + w.substring(0, 3).toUpperCase();
    });
    return {
      en: mockEnVec.join(" "),
      ar: clean
    };
  } else {
    // Translate word-by-word or use generic pattern
    const words = lower.split(/[\s&/]+/);
    const translatedWords = words.map(w => dict[w] || `${w.charAt(0).toUpperCase() + w.slice(1)}`);
    arResult = "مجموعة " + translatedWords.map(w => dict[w.toLowerCase()] || w).join(" ");
  }

  return {
    en: clean.toUpperCase(),
    ar: arResult
  };
}

